/**
 * Escapes text for safe interpolation into `innerHTML`
 */
export function escapeHtml(s: string): string {
    return String(s)
        .replaceAll('&', '&amp;')
        .replaceAll('<', '&lt;')
        .replaceAll('>', '&gt;')
        .replaceAll('"', '&quot;')
        .replaceAll("'", '&#039;');
}
